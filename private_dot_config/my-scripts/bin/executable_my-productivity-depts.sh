#!/usr/bin/env bash

# Check the external commands and files used by the managed Nushell config.
# Referenced helper scripts are checked as leaf dependencies; their own
# transitive dependencies are intentionally outside this audit's scope.

set -uo pipefail

script_name="${0##*/}"
show_only_missing=false
category_filter=""
use_color=true

usage() {
  cat <<'EOF'
Usage: my-productivity-depts.sh [OPTIONS]

Check dependencies used by the managed Nushell configuration.

Options:
  -c, --category CATEGORY  Show only required, optional, or misc dependencies
  -m, --missing            Show only missing dependencies
      --no-color           Disable ANSI colors
  -h, --help               Show this help

The command exits with status 1 only when a required dependency is missing.
EOF
}

while (($# > 0)); do
  case "$1" in
    -c|--category)
      if (($# < 2)); then
        printf '%s: %s requires an argument\n' "$script_name" "$1" >&2
        exit 2
      fi
      category_filter="$2"
      shift 2
      ;;
    --category=*)
      category_filter="${1#*=}"
      shift
      ;;
    -m|--missing)
      show_only_missing=true
      shift
      ;;
    --no-color)
      use_color=false
      shift
      ;;
    -h|--help)
      usage
      exit 0
      ;;
    *)
      printf '%s: unknown option: %s\n' "$script_name" "$1" >&2
      usage >&2
      exit 2
      ;;
  esac
done

case "$category_filter" in
  ""|required|optional|misc) ;;
  *)
    printf '%s: invalid category: %s\n' "$script_name" "$category_filter" >&2
    exit 2
    ;;
esac

if $use_color; then
  reset=$'\033[0m'
  bold=$'\033[1m'
  dim=$'\033[2m'
  red=$'\033[31m'
  green=$'\033[32m'
  yellow=$'\033[33m'
  blue=$'\033[34m'
  magenta=$'\033[35m'
  cyan=$'\033[36m'
else
  reset=""
  bold=""
  dim=""
  red=""
  green=""
  yellow=""
  blue=""
  magenta=""
  cyan=""
fi

separator=$'\034'
declare -a dependencies=()
declare -a results=()

add_dependency() {
  local category="$1"
  local kind="$2"
  local name="$3"
  local target="$4"
  local purpose="$5"
  dependencies+=("${category}${separator}${kind}${separator}${name}${separator}${target}${separator}${purpose}")
}

config_home="${XDG_CONFIG_HOME:-${HOME}/.config}"
nu_config_dir="${config_home}/nushell"

# Required: needed to run or parse the configured interactive shell.
add_dependency required command nu nu "Nushell runtime"
add_dependency required command tty tty "Unconditional terminal detection in config.nu"
add_dependency required command nproc nproc "Unconditional MAKEFLAGS setup in config.nu"
add_dependency required file config.nu "${nu_config_dir}/config.nu" "Primary Nushell configuration"
add_dependency required file env.nu "${nu_config_dir}/env.nu" "Pre-config environment setup"
add_dependency required file alias.nu "${nu_config_dir}/alias.nu" "Unconditionally sourced aliases and commands"
add_dependency required file archlinux.nu "${nu_config_dir}/archlinux.nu" "Parser-time source target, even outside Arch"
add_dependency required file bookmark.nu "${nu_config_dir}/bookmark.nu" "Sourced by alias.nu at parse time"
add_dependency required file completions.nu "${nu_config_dir}/completions.nu" "Unconditionally sourced completion setup"
add_dependency required file dir-history.nu "${nu_config_dir}/dir-history.nu" "Unconditionally sourced directory history"
add_dependency required file file-picker.nu "${nu_config_dir}/file-picker.nu" "Unconditionally sourced file-picker commands"
add_dependency required file zellij.nu "${nu_config_dir}/zellij.nu" "Unconditionally sourced Zellij commands"
add_dependency required file mise.nu "${nu_config_dir}/mise.nu" "Generated by env.nu and unconditionally used by config.nu"

# Optional: guarded, platform-specific, or feature-level integrations.
add_dependency optional command atuin atuin "Atuin history integration"
add_dependency optional command dust dust "Atuin history integration"
add_dependency optional command carapace carapace "External command completion backend"
add_dependency optional command direnv direnv "Per-directory environment hook"
add_dependency optional command eza eza "Enhanced directory listings"
add_dependency optional command fastfetch fastfetch "Interactive startup decoration"
add_dependency optional command fd fd "Directory counting and fuzzy navigation"
add_dependency optional command fzf fzf "Bookmarks, history, layouts, and pickers"
add_dependency optional command git git "Repository navigation and Zellij tab names"
add_dependency optional command head head "Large-directory listing limiter"
add_dependency optional command less less "Paged file listings"
add_dependency optional command mise mise "Tool-version environment activation"
add_dependency optional command nvim nvim "Configured editor"
add_dependency optional command sccache sccache "Rust compiler cache wrapper"
add_dependency optional command starship starship "Prompt initialization"
add_dependency optional command tput tput "Terminal width lookup"
add_dependency optional command topiary topiary "Formatter configured through environment variables"
add_dependency optional command wc wc "Directory entry counting"
add_dependency optional command zellij zellij "Terminal multiplexer integration"
add_dependency optional command zoxide zoxide "Directory jumping integration"
add_dependency optional command file-picker.sh file-picker.sh "Leaf helper used by file-picker.nu"
add_dependency optional command nvim-nu.sh nvim-nu.sh "Configured Nushell buffer editor"
add_dependency optional file atuin-init.nu "${HOME}/.local/share/atuin/init.nu" "Optional Atuin-generated init file"
add_dependency optional file cargo-env.nu "${HOME}/.cargo/env.nu" "Optional Cargo environment file"
add_dependency optional file eza.nu "${nu_config_dir}/eza.nu" "Optional enhanced-listing definitions"
add_dependency optional file my-cubrid-aliases.nu "${HOME}/my-cubrid/aliases.nu" "Optional private CUBRID aliases"
add_dependency optional file no-browser "${HOME}/.local/bin/no-browser" "Browser suppression helper used in SSH sessions"
add_dependency optional file topiary-config "${config_home}/topiary/languages.ncl" "Configured Topiary language file"
add_dependency optional file topiary-languages "${config_home}/topiary/languages" "Configured Topiary language directory"
add_dependency optional file vcpkg "${HOME}/vcpkg" "Optional Vcpkg root"
add_dependency optional file zellij-layouts "${config_home}/zellij/layouts" "Layout source used by zellij-new"
add_dependency optional file zoxide.nu "${HOME}/.zoxide.nu" "Optional Zoxide-generated init file"

kernel_name="$(uname -s 2>/dev/null || printf 'unknown')"
if [[ "$kernel_name" == Linux ]]; then
  add_dependency optional command xclip xclip "Clipboard backend currently invoked by clip"
  add_dependency misc command wl-copy wl-copy "Detected clipboard alternative; see nushell-clip-defect.md"

  if grep -qi microsoft /proc/version 2>/dev/null; then
    add_dependency optional command wslpath wslpath "Windows USERPROFILE conversion under WSL"
    add_dependency optional command clip.exe clip.exe "Intended WSL clipboard backend"
  fi

  if [[ -r /etc/os-release ]] && grep -q '^ID=arch\|^ID_LIKE=.*arch' /etc/os-release; then
    add_dependency optional command kime kime "Preferred Arch input method"
    add_dependency optional command fcitx5 fcitx5 "Fallback Arch input method"
  fi
elif [[ "$kernel_name" == Darwin ]]; then
  add_dependency optional command pbcopy pbcopy "macOS clipboard backend"
fi

# Misc: tools used only by individual aliases or manually invoked helpers.
add_dependency misc command bat bat "Help-text viewer"
add_dependency misc command cargo cargo "Rust installer helper"
add_dependency misc command chezmoi chezmoi "Dotfile aliases"
add_dependency misc command claude claude "Claude aliases"
add_dependency misc command curl curl "Connectivity check alias"
add_dependency misc command diffnav diffnav "Chezmoi diff viewer"
add_dependency misc command fzf-make fzf-make "Fuzzy make-target alias"
add_dependency misc command gh gh "GitHub CLI aliases and dashboard"
add_dependency misc command git-diffnav git-diffnav "Git diff selection helper"
add_dependency misc command git-diffview git-diffview "Git Diffview selection helper"
add_dependency misc command gsettings gsettings "GNOME Caps Lock remapping"
add_dependency misc command hgrep hgrep "Help search alias"
add_dependency misc command just just "Recipe aliases and pickers"
add_dependency misc command lazydocker lazydocker "Container TUI aliases"
add_dependency misc command lazygit lazygit "Git TUI alias"
add_dependency misc command lazyjj lazyjj "Jujutsu TUI alias"
add_dependency misc command rg rg "Ripgrep aliases"
add_dependency misc command somo somo "Port inspection aliases"
add_dependency misc command sudo sudo "Privileged aliases"
add_dependency misc command systemctl systemctl "Systemd alias"
add_dependency misc command sysctl sysctl "Performance-event alias"
add_dependency misc command tmux tmux "Tmux aliases"
add_dependency misc command tspin tspin "Terminal spinner alias"
add_dependency misc command upower upower "Battery picker alias"
add_dependency misc command xdg-open xdg-open "Desktop opener alias"
add_dependency misc command yazi yazi "File-manager integration"

add_dependency misc command autorun.sh autorun.sh "Leaf helper referenced by alias x"
add_dependency misc command cargo-run-example.sh cargo-run-example.sh "Leaf helper referenced by alias e"
add_dependency misc command crc.sh crc.sh "Leaf helper referenced by alias crc"
add_dependency misc command detect-project.sh detect-project.sh "Leaf helper referenced by xe"
add_dependency misc command gh-clone-fuzzy.sh gh-clone-fuzzy.sh "Leaf helper referenced by alias ghcf"
add_dependency misc command git-clone-user.sh git-clone-user.sh "Leaf helper referenced by alias ghcu"
add_dependency misc command git-log-find.sh git-log-find.sh "Leaf helper referenced by alias glf"
add_dependency misc command git-log-pr.sh git-log-pr.sh "Leaf helper referenced by alias glpr"
add_dependency misc command git-log.sh git-log.sh "Leaf helper referenced by log aliases"
add_dependency misc command git-rebase-pr.sh git-rebase-pr.sh "Leaf helper referenced by alias grbpr"
add_dependency misc command github-clone-confirm.sh github-clone-confirm.sh "Leaf helper referenced by alias ghc"
add_dependency misc command just-pick.nu just-pick.nu "Leaf helper referenced by jj"
add_dependency misc command just.nu just.nu "Leaf helper referenced by n and ni"
add_dependency misc command mdserve-fzf.sh mdserve-fzf.sh "Leaf helper referenced by alias mdsf"
add_dependency misc command my-cargo-install.nu my-cargo-install.nu "Leaf helper referenced by its matching alias"
add_dependency misc command ps-fuzzy.sh ps-fuzzy.sh "Leaf helper referenced by alias pf"
add_dependency misc command rerun-on-enter.nu rerun-on-enter.nu "Leaf helper referenced by alias re"
add_dependency misc command rgf.sh rgf.sh "Leaf helper referenced by alias rgf"
add_dependency misc command zellij-attach-or-new.sh zellij-attach-or-new.sh "Leaf helper referenced by za and zv"

declare -A found_count=( [required]=0 [optional]=0 [misc]=0 )
declare -A missing_count=( [required]=0 [optional]=0 [misc]=0 )

for dependency in "${dependencies[@]}"; do
  IFS="$separator" read -r category kind name target purpose <<< "$dependency"
  found=false
  detail=""

  case "$kind" in
    command)
      if resolved_path="$(command -v -- "$target" 2>/dev/null)"; then
        found=true
        detail="$resolved_path"
      else
        detail="not found on PATH"
      fi
      ;;
    file)
      if [[ -e "$target" ]]; then
        found=true
        detail="$target"
      else
        detail="missing: $target"
      fi
      ;;
  esac

  if $found; then
    ((found_count[$category] += 1))
    status=found
  else
    ((missing_count[$category] += 1))
    status=missing
  fi

  results+=("${category}${separator}${name}${separator}${status}${separator}${detail}${separator}${purpose}")
done

printf '%bNushell dependency check%b\n' "${bold}${cyan}" "$reset"
printf '%bScope:%b %s/*.nu + referenced helper scripts (leaf checks)\n' "$dim" "$reset" "$nu_config_dir"
printf '%bHost:%b  %s\n\n' "$dim" "$reset" "$kernel_name"

for category in required optional misc; do
  if [[ -n "$category_filter" && "$category_filter" != "$category" ]]; then
    continue
  fi

  case "$category" in
    required) header_color="$red" ;;
    optional) header_color="$yellow" ;;
    misc) header_color="$magenta" ;;
  esac

  printf '%b%s%b  %b%d found, %d missing%b\n' \
    "${bold}${header_color}" "${category^^}" "$reset" "$dim" \
    "${found_count[$category]}" "${missing_count[$category]}" "$reset"

  rendered=0
  for result in "${results[@]}"; do
    IFS="$separator" read -r result_category name status detail purpose <<< "$result"
    [[ "$result_category" == "$category" ]] || continue
    if $show_only_missing && [[ "$status" == found ]]; then
      continue
    fi

    if [[ "$status" == found ]]; then
      symbol="${green}✓${reset}"
      name_color="$green"
    elif [[ "$category" == required ]]; then
      symbol="${red}✗${reset}"
      name_color="$red"
    elif [[ "$category" == optional ]]; then
      symbol="${yellow}○${reset}"
      name_color="$yellow"
    else
      symbol="${dim}·${reset}"
      name_color="$dim"
    fi

    printf '  %b %b%-28s%b %s\n' "$symbol" "$name_color" "$name" "$reset" "$purpose"
    printf '    %b%s%b\n' "$dim" "$detail" "$reset"
    ((rendered += 1))
  done

  if ((rendered == 0)); then
    printf '  %bNo matching dependencies.%b\n' "$dim" "$reset"
  fi
  printf '\n'
done

if [[ -n "$category_filter" ]]; then
  total_found="${found_count[$category_filter]}"
  total_missing="${missing_count[$category_filter]}"
else
  total_found=$((found_count[required] + found_count[optional] + found_count[misc]))
  total_missing=$((missing_count[required] + missing_count[optional] + missing_count[misc]))
fi
printf '%bSUMMARY%b  %b%d found%b  %b%d missing%b\n' \
  "${bold}${blue}" "$reset" "$green" "$total_found" "$reset" \
  "$yellow" "$total_missing" "$reset"

if ((missing_count[required] > 0)); then
  printf '%bRequired dependencies are missing.%b\n' "$red" "$reset"
  exit 1
fi
